import { NextRequest, NextResponse } from 'next/server'

export async function POST(request: NextRequest) {
  try {
    const body = await request.json()
    const { name, phone, email, brand, model, year, mileage, vin, comment } = body

    // –ü–æ–ª—É—á–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
    const botToken = process.env.TELEGRAM_BOT_TOKEN
    const chatId = process.env.TELEGRAM_CHAT_ID

    if (!botToken || !chatId) {
      return NextResponse.json(
        { error: 'Telegram configuration missing' },
        { status: 500 }
      )
    }

    // –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    let text = `üí∞ *–ó–∞—è–≤–∫–∞ –Ω–∞ –ø—Ä–æ–¥–∞–∂—É –∞–≤—Ç–æ–º–æ–±–∏–ª—è*\n\n`
    text += `üë§ *–ò–º—è:* ${name}\n`
    text += `üìû *–¢–µ–ª–µ—Ñ–æ–Ω:* ${phone}\n`
    if (email) text += `üìß *Email:* ${email}\n`
    text += `\nüöó *–ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ–± –∞–≤—Ç–æ–º–æ–±–∏–ª–µ:*\n`
    text += `‚Ä¢ *–ú–∞—Ä–∫–∞:* ${brand}\n`
    text += `‚Ä¢ *–ú–æ–¥–µ–ª—å:* ${model}\n`
    text += `‚Ä¢ *–ì–æ–¥:* ${year}\n`
    text += `‚Ä¢ *–ü—Ä–æ–±–µ–≥:* ${mileage} –∫–º\n`
    if (vin) text += `‚Ä¢ *VIN:* ${vin}\n`
    if (comment) text += `\nüí¨ *–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π:* ${comment}\n`
    text += `\n‚è∞ *–í—Ä–µ–º—è:* ${new Date().toLocaleString('ru-RU', { timeZone: 'Europe/Minsk' })}`

    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –≤ Telegram
    const telegramUrl = `https://api.telegram.org/bot${botToken}/sendMessage`
    const telegramResponse = await fetch(telegramUrl, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        chat_id: chatId,
        text,
        parse_mode: 'Markdown',
      }),
    })

    if (!telegramResponse.ok) {
      throw new Error('Failed to send message to Telegram')
    }

    return NextResponse.json({ success: true })
  } catch (error) {
    console.error('Telegram API error:', error)
    return NextResponse.json(
      { error: 'Failed to send message' },
      { status: 500 }
    )
  }
}
